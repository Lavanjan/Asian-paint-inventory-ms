     public void Show_details_staff()
    {
     ArrayList<Display_staff_and_edit> list = getStafflist();
     DefaultTableModel model = (DefaultTableModel)jTable_display_users.getModel();
     Object[] row = new Object[9];
     for(int i = 0; i<list.size();i++)
     {
         row[0] = list.get(i).getID();
         row[1] = list.get(i).getfname();
         row[2] = list.get(i).getlname();
         row[3] = list.get(i).getuname();
         row[4] = list.get(i).getpass();
         row[5] = list.get(i).getAddr();
         row[6] = list.get(i).getnum();
         row[7] = list.get(i).getgen();
         row[8] = list.get(i).getroll();
         
         model.addRow(row);
         
         
     }
    }
     
     public void excuteSQlQuery(String query, String message)
     {
         Connection con = getConnection();
         Statement st;
         try {
             st = con.createStatement();
             if((st.executeUpdate(query)) == 1)
             {
                 // Refresh Data
                 DefaultTableModel model = (DefaultTableModel)jTable_display_users.getModel();
                 model.setRowCount(0);
                 Show_details_staff();
                 
                 JOptionPane.showMessageDialog(null, "Dta "+message+" Successfully");
             }else{
                 JOptionPane.showMessageDialog(null, "Dta Not"+message);
             }
         } catch (Exception ex) {
             ex.printStackTrace();
         }
     }
    
   
    
    
    
    
    
    
    
    
    

