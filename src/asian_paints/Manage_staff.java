    public ArrayList<Display_staff_and_edit> getStafflist()
    {
        ArrayList<Display_staff_and_edit> stafflist = new ArrayList<Display_staff_and_edit>();
        Connection connection = getConnection();
        String query = "SELECT * FROM `login`";
        Statement st;
        ResultSet rs;
        
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Display_staff_and_edit staff;
            
            while(rs.next())
            {
                staff = new Display_staff_and_edit(rs.getInt("empid"),rs.getString("firstname"),rs.getString("lastname"),rs.getString("username"),rs.getString("password"),rs.getString("address"),rs.getString("number"),rs.getString("gender"),rs.getString("roll"));
                stafflist.add(staff);
            }
        } catch (Exception e) {
        }
        return stafflist;
    }
    
